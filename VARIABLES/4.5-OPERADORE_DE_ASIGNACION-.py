# LOS VALORES DE ASIGNACION SIRVEN PARA CORTAR EL CODIGO

'''para poder usar operadores de asignacion previamente debemos declarar
una variable y asignado un valor inicial, porque estos solo sirven para
aumentar, disminuir, dividir, multiplicar a lo que ya tenemos'''

'''AQUI EN EL EJEMPLO VEMOS UNA VARIABLE CON UN VALOR ASIGNADO, LOS OPERADORES DE ASIGNACION 
VAN A UTILIZAR A ESTA PARA APLICAR EL RESTO DE LAS FUNCIONES QUE HARA QUE AUMENTE O DISMINUYA
SEGUN LE QUE LE ASIGNEMOS'''
a=2   #VARIABLE CON SU VALOR ASIGNADO
a=a+5 # esta es la forma larga de escribir una operacion de asignacion
print(a)

a=2   # a continuacion utilizamos los metodos acortados de los operadores de asignacion
a+=5  #suma en asignacion
print(a)
a-=2  #resta en asignacion
print(a)
a*=3 #multiplicacion por asignacion
print(a)
a/=3 #division en asignacion
print(a)
a**=2 #potenciacion en asignacion
print(a)
a%=2 #modulo en asignacion da como resultado el resto de la division
print(a)

